if ( isObject( gameMap ) )
   gameMap.delete();
new ActionMap(gameMap);

//------------------------------------------------------------------------------
// Camera & View functions
//------------------------------------------------------------------------------

function toggleCamera(%val)
{
   if (%val)
      commandToServer('ToggleCamera');
}

gameMap.bind(keyboard, "alt c", toggleCamera);
gameMap.bind( gamepad, btn_back, toggleCamera );

//------------------------------------------------------------------------------
// Demo recording functions
//------------------------------------------------------------------------------

function startRecordingDemo( %val )
{
   if ( %val )
      startDemoRecord();
}

function stopRecordingDemo( %val )
{
   if ( %val )
      stopDemoRecord();
}

gameMap.bind( keyboard, F3, startRecordingDemo );
gameMap.bind( keyboard, F4, stopRecordingDemo );

//------------------------------------------------------------------------------
// Helper Functions
//------------------------------------------------------------------------------

function dropCameraAtPlayer(%val)
{
   if (%val)
      commandToServer('dropCameraAtPlayer');
}

function dropPlayerAtCamera(%val)
{
   if (%val)
      commandToServer('DropPlayerAtCamera');
}

gameMap.bind(keyboard, "F8", dropCameraAtPlayer);
gameMap.bind(keyboard, "F7", dropPlayerAtCamera);
